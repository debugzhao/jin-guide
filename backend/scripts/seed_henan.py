async def run_seed(db_url: str = DATABASE_URL) -> None:
    engine = create_async_engine(db_url, echo=False)

    async with AsyncSession(engine) as session:
        # 清除旧数据（幂等）
        await session.execute(text("DELETE FROM subject_requirements"))
        await session.execute(text("DELETE FROM admission_scores"))
        await session.execute(text("DELETE FROM rank_segments"))
        await session.execute(text("DELETE FROM universities"))
        await session.commit()

        # ── 插入院校 ────────────────────────────────────────────────────────
        univ_id_map: dict[str, str] = {}
        for row in UNIVERSITIES:
            key, name, code, city, prov, stype, is985, is211, issyl, hasmed, tmin, tmax = row
            uid = str(uuid.uuid4())
            univ_id_map[key] = uid
            await session.execute(text("""
                INSERT INTO universities (id, name, code, city, province, school_type,
                    is_985, is_211, is_shuangyiliu, has_medical_program,
                    annual_tuition_min, annual_tuition_max)
                VALUES (:id, :name, :code, :city, :province, :school_type,
                    :is_985, :is_211, :is_shuangyiliu, :has_medical_program,
                    :tuition_min, :tuition_max)
            """), {
                "id": uid, "name": name, "code": code, "city": city, "province": prov,
                "school_type": stype, "is_985": is985, "is_211": is211,
                "is_shuangyiliu": issyl, "has_medical_program": hasmed,
                "tuition_min": tmin, "tuition_max": tmax,
            })

        await session.commit()
        print(f"✓ 插入 {len(UNIVERSITIES)} 所院校")

        # ── 插入录取分数线 ──────────────────────────────────────────────────
        count = 0
        for row in ADMISSION_SCORES_PHYSICS:
            ukey, year, min_s, min_r, avg_s, avg_r, max_s, enroll = row
            if ukey not in univ_id_map:
                continue
            await session.execute(text("""
                INSERT INTO admission_scores
                    (id, university_id, year, province, batch, subject_type,
                     min_score, min_rank, avg_score, avg_rank, max_score, enrollment_count)
                VALUES (:id, :univ_id, :year, '河南', '本科批', 'physics',
                        :min_s, :min_r, :avg_s, :avg_r, :max_s, :enroll)
            """), {
                "id": str(uuid.uuid4()), "univ_id": univ_id_map[ukey], "year": year,
                "min_s": min_s, "min_r": min_r, "avg_s": avg_s,
                "avg_r": avg_r, "max_s": max_s, "enroll": enroll,
            })
            count += 1
        await session.commit()
        print(f"✓ 插入 {count} 条录取分数线")

        # ── 插入位次段 ──────────────────────────────────────────────────────
        seg_count = 0
        for segs, year in [(RANK_SEGMENTS_2025, 2025), (RANK_SEGMENTS_2024, 2024), (RANK_SEGMENTS_2023, 2023)]:
            for score, rank in segs:
                await session.execute(text("""
                    INSERT INTO rank_segments (id, year, province, subject_type, score, cumulative_rank)
                    VALUES (:id, :year, '河南', 'physics', :score, :rank)
                """), {"id": str(uuid.uuid4()), "year": year, "score": score, "rank": rank})
                seg_count += 1
        await session.commit()
        print(f"✓ 插入 {seg_count} 条位次段数据")

        # ── 插入选科要求 ────────────────────────────────────────────────────
        req_count = 0
        for row in SUBJECT_REQUIREMENTS:
            ukey, major, req, opt, opt_cnt, restricted, medical = row
            if ukey not in univ_id_map:
                continue
            await session.execute(text("""
                INSERT INTO subject_requirements
                    (id, university_id, major_name, required_subjects,
                     optional_subjects, optional_required_count,
                     restricted_subjects, medical_restrictions)
                VALUES (:id, :univ_id, :major, :req, :opt, :opt_cnt, :restricted, :medical)
            """), {
                "id": str(uuid.uuid4()), "univ_id": univ_id_map[ukey], "major": major,
                "req": json.dumps(req, ensure_ascii=False),
                "opt": json.dumps(opt, ensure_ascii=False),
                "opt_cnt": opt_cnt,
                "restricted": json.dumps(restricted, ensure_ascii=False),
                "medical": json.dumps(medical, ensure_ascii=False) if medical else None,
            })
            req_count += 1
        await session.commit()
        print(f"✓ 插入 {req_count} 条选科要求")

    await engine.dispose()
    print("\n河南省种子数据导入完成 ✓")


if __name__ == "__main__":
    asyncio.run(run_seed())
